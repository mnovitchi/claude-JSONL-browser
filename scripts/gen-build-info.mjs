// Generates lib/build-info.ts with the build's identity: the canonical SemVer
// (from package.json), the current git short-hash, and the build date. This file
// is gitignored and regenerated before every `dev` and `build` (npm pre* hooks),
// so hand-built local artifacts are always traceable. Pure Node, no deps.
//
// Degrades gracefully: if git is unavailable (fresh download, no .git), the hash
// falls back to 'unknown' and the build still succeeds.

import { execSync } from 'node:child_process'
import { readFileSync, writeFileSync } from 'node:fs'

const pkg = JSON.parse(readFileSync('package.json', 'utf8'))
const version = pkg.version

let gitHash = 'unknown'
try {
  gitHash = execSync('git rev-parse --short HEAD', { stdio: ['ignore', 'pipe', 'ignore'] })
    .toString()
    .trim()
} catch {
  // No git / not a repo — leave as 'unknown'.
}

// YYYY-MM-DD in the local timezone.
const buildDate = new Date().toISOString().slice(0, 10)

const contents = `// AUTO-GENERATED by scripts/gen-build-info.mjs — do not edit, do not commit.
// Regenerated before every dev/build. See the "Releasing" section in CLAUDE.md.
export const BUILD_INFO = {
  version: ${JSON.stringify(version)},
  gitHash: ${JSON.stringify(gitHash)},
  buildDate: ${JSON.stringify(buildDate)},
} as const
`

writeFileSync('lib/build-info.ts', contents)
console.log(`Wrote lib/build-info.ts — v${version} (${gitHash} · ${buildDate})`)
